# Tuples: like list, it also stores a list of object
# It is immutable/cannot be change once created
# It doesn't have method like List that can replace/insert value
# Summary, Tuples is only readable, not editable

# Count: The value appears in the list for how many times
numbers = (1, 2, 3, 2)
print(numbers.count(2))

# Index: The value appears in which position on the list
numbers = (1, 2, 3, 2)
print(numbers.index(1))
