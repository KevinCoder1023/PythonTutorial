fruits = ["Apple", "Grape", "Orange"]
# Append: insert value at the last position of the list
fruits.append("Peal")
print(fruits)
# Insert: insert value to the list at the selected position
fruits.insert(0, "Kiwi")
print(fruits)
# Remove: Remove value from the list
fruits.remove("Kiwi")
print(fruits)
# in: Check is value is in the list
print("Kiwi" in fruits)
# not in: Check is value is not in the list
print("Kiwi" not in fruits)
# len : Length of the list
print(len(fruits))
# Clear: Clear the list
fruits.clear()
print(fruits)

