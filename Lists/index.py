# List: used to display list of objects
names = ['A', 'B', 'Top G', 'C', 'John']
print(names)  # return the list
print(names[0])  # return the FIRST item of the list
print(names[-1])  # return the LAST item of the list
print(names[-2])  # return the SECOND LAST item of the list
print(names[2])  # return the SECOND item of the list
names[0] = "Ball"  # replace the selected item to a new value
print(names[0:3])  # return the first index to and exlcude end index
