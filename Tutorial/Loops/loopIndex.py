# For loop:
numbers = [1, 2, 3, 4, 5]
for item in numbers:
    print(item)

# While loop:
i = 1
# It will keep on loop while the number is less than or equals to 5
while i <= 5:
    print(i)
    i += 1  # Increment to ensure the value will keep on adding and prevent infinite loop

z = 1
# It will return a string and it will be repeat based on the number
while z <= 10:
    print(z * "&")
    z += 1
