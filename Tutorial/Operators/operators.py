print(10+3) # addition
print(10-3) # subtraction
print(10*3) # multiplication
print(10/3) # division: return a decimal/float
print(10 // 3) # division: return a integer
print(10 ** 3) # Power of

# Increment the value by (add, subtract, multiply, divide):
x = 10
x+=3 # it will increment the x value by 3 ( x + 3)
print(x) # it will give you 13

# Operators Precedence/order:
# Similar to basic maths: multiply/divide come first, add/subtract come second
y = 10+3*2
print(y)
